
package minggu3;


public class while_ulang {

    public static void main(String[] args) {
        int counter=0;
        System.out.println("\n PROGRAM DEMO WHILE");
        System.out.println("-------------------");
        while (counter<=5)
        {
            System.out.println("Mencetak Counter ke- "+counter);
            counter=counter+1;
        }
    }
    
}
